
x = 10
y = 5
f = True
k = False

# Arithmetic operators
add = x + y
sub = x - y
mul = x * y
division = x / y
mod = x % y
print("Addition:", add)
print("Subtraction:", sub)
print("Multiplication:", mul)
print("Division:", division) #quotient
print("Modulus:", mod) #remainder

# Comparison operators


print("x > y:", x > y)
print("x < y:", x < y)
print("x >= y:", x >= y)
print("x <= y:", x <= y)
print("x == y:", x == y)
print("x != y:", x != y)

# Logical operators


print("p and q:", f and k)
print("p or q:", f or k)
print("not p:", not k)
